package entidad;

/**
 *
 * @author Usuario
 */
public class Producto {
    private String name;
    private Double price;

    public Producto() {
    }

    public Producto(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }
    
}
